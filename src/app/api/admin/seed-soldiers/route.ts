export async function POST() {
  try {
    const session = await auth();
    if (!session?.user?.email) return NextResponse.json({ error: "Unauthorized" }, { status: 401 });
    const user = getUserByEmail(session.user.email);
    if (!user || user.role !== "admin") return NextResponse.json({ error: "Unauthorized" }, { status: 401 });

    // Skip if soldiers already exist
    const existing = getSoldiers();
    if (existing.length > 0) {
      return NextResponse.json({ message: `Already have ${existing.length} soldiers, skipping seed.` });
    }

    const seenIds = new Set<string>();
    let created = 0;

    const lines = CSV_DATA.split("\n").filter((line) => line.trim());

    for (const line of lines) {
      const parts = line.split(",").map((s) => s.trim());
      if (parts.length < 3) continue;

      const [name, personalId, role, phone, email, region, city, address, birthDate, serviceEndDate, unitJoinDate] = parts;

      // Skip duplicates
      if (seenIds.has(personalId)) continue;
      seenIds.add(personalId);

      createSoldier({
        name: name || "",
        personalId: personalId || "",
        role: role || "",
        phone: phone || undefined,
        email: email ? email.toLowerCase() : undefined,
        region: region || undefined,
        city: city || undefined,
        address: address || undefined,
        birthDate: birthDate ? convertDate(birthDate) : undefined,
        serviceEndDate: serviceEndDate ? convertDate(serviceEndDate) : undefined,
        unitJoinDate: unitJoinDate ? convertDate(unitJoinDate) : undefined,
      });
      created++;
    }

    return NextResponse.json({ message: `Seeded ${created} soldiers successfully!` });
  } catch (error) {
    console.error("Seed soldiers error:", error);
    return NextResponse.json({ error: "Internal server error" }, { status: 500 });
  }
}
