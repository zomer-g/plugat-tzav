function convertDate(ddmmyyyy: string): string {
  if (!ddmmyyyy || !ddmmyyyy.trim()) return "";
  const parts = ddmmyyyy.trim().split("/");
  if (parts.length !== 3) return "";
  const [dd, mm, yyyy] = parts;
  return `${yyyy}-${mm.padStart(2, "0")}-${dd.padStart(2, "0")}`;
}

